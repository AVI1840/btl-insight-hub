export function findQueryResult(question: string): QueryResult | null {
  const exact = mockQueryResults[question]
  if (exact) return exact

  const normalQ = question.replace(/[?؟\s]+/g, ' ').trim()
  for (const [key, val] of Object.entries(mockQueryResults)) {
    const normalK = key.replace(/[?؟\s]+/g, ' ').trim()
    if (normalQ.includes(normalK.slice(0, 15)) || normalK.includes(normalQ.slice(0, 15))) {
      return val
    }
  }

  return {
    question,
    answer: `לא נמצאה תשובה ספציפית לשאלה "${question}".\n\nנסה לנסח את השאלה אחרת, או בחר מהשאלות המוצעות. המערכת מנתחת נתונים מ-82,450 פניות ב-12 אגפים.`,
    sources: [],
    confidence: 0,
    relatedQuestions: suggestedQueries.slice(0, 3),
  }
}
